-- 查看 Supabase 中所有 Cron 任务
-- ============================================

-- 查看所有 Cron 任务
SELECT 
  jobid as "任务ID",
  jobname as "任务名称",
  schedule as "执行计划",
  active as "是否激活",
  command as "执行命令"
FROM cron.job
ORDER BY jobid;

-- ============================================
-- 时间对照表
-- ============================================
-- 
-- Cron 表达式 | UTC 时间 | 北京时间
-- ------------|---------|----------
-- 0 * * * *   | 每小时   | 每小时
-- 0 16 * * *  | 16:00   | 00:00（次日）
-- 0 22 * * *  | 22:00   | 06:00（次日）
-- 
-- ============================================
-- 说明
-- ============================================
-- 
-- 你应该看到 2 个任务：
-- 1. hourly-snapshot - 每小时快照（0 * * * *）
-- 2. daily-archive - 每日归档（0 16 * * * 或 0 22 * * *）
