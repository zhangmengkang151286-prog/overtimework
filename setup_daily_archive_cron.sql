-- 设置每日自动归档任务
-- 每天23:59自动将当天的数据归档到 daily_history 表

-- 1. 启用 pg_cron 扩展（需要在 Supabase Dashboard 中启用）
-- 注意：pg_cron 扩展需要在 Supabase 项目设置中启用
-- 路径：Database > Extensions > 搜索 "pg_cron" > 启用

-- 2. 创建定时任务
-- 每天23:59执行 archive_daily_data 函数
SELECT cron.schedule(
  'daily-archive-job',           -- 任务名称
  '59 23 * * *',                 -- Cron 表达式：每天23:59
  $$SELECT archive_daily_data()$$  -- 执行的 SQL
);

-- 3. 查看已创建的定时任务
SELECT * FROM cron.job;

-- 4. 查看定时任务执行历史
SELECT * FROM cron.job_run_details 
ORDER BY start_time DESC 
LIMIT 10;

-- ============================================
-- 如果需要删除定时任务，使用以下命令：
-- ============================================
-- SELECT cron.unschedule('daily-archive-job');

-- ============================================
-- 如果需要手动执行归档函数（用于测试）：
-- ============================================
-- SELECT archive_daily_data();

-- ============================================
-- Cron 表达式说明：
-- ============================================
-- 格式：分 时 日 月 星期
-- '59 23 * * *' 表示：
--   - 59 分钟
--   - 23 点（晚上11点）
--   - * 每天
--   - * 每月
--   - * 每周的每一天
--
-- 其他示例：
-- '0 0 * * *'   - 每天午夜00:00
-- '30 2 * * *'  - 每天凌晨02:30
-- '0 */6 * * *' - 每6小时执行一次
-- '0 9 * * 1'   - 每周一上午09:00

